# Assign values to variables
x = 10               
name = "Alice"       
pi = 3.14           
is_valid = True      

# Print variable values
print("x =", x)
print("name =", name)
print("pi =", pi)
print("is_valid =", is_valid)

# Variables can be reassigned
x = 20
print("New value of x =", x)

# Multiple assignment
a, b, c = 1, 2, 3
print("a =", a, "b =", b, "c =", c)

