text = "Hello, World!"

print("Original string:", text)
print("text[0:5]:", text[0:5])       
print("text[7:]:", text[7:])         
print("text[:5]:", text[:5])         
print("text[-6:-1]:", text[-6:-1])   
print("text[::2]:", text[::2])       
print("text[::-1]:", text[::-1])     

print("\n")

